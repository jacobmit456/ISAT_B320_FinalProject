-- Populate Tables --
-- Create Tables 
CREATE TABLE [RevenueCategories] (
  [RevenueCategoryID] INT IDENTITY (1,1) NOT NULL,
  [Category] VARCHAR(200),
  [Description] VARCHAR(200),
  PRIMARY KEY ([RevenueCategoryID])
);

CREATE TABLE [EventLocations] (
  [LocationID] INT IDENTITY (1,1) NOT NULL,
  [Location] VARCHAR(200),
  [Description] VARCHAR(200),
  PRIMARY KEY ([LocationID])
);

CREATE TABLE [Events] (
  [EventID] INT IDENTITY (1,1) NOT NULL,
  [LocationID] INT,
  [EventTitle] VARCHAR(MAX),
  [EventDate] DATE,
  [Description] VARCHAR(200),
  PRIMARY KEY ([EventID]),
  CONSTRAINT [FK_Events.LocationID]
    FOREIGN KEY ([LocationID])
      REFERENCES [EventLocations]([LocationID])
);

CREATE TABLE [Revenue] (
  [RevenueID] INT IDENTITY (1,1) NOT NULL,
  [EventID] INT,
  [RevenueCategoryID] INT,
  [Amount] MONEY,
  [Description] VARCHAR(MAX),
  PRIMARY KEY ([RevenueID]),
  CONSTRAINT [FK_Revenue.RevenueCategoryID]
    FOREIGN KEY ([RevenueCategoryID])
      REFERENCES [RevenueCategories]([RevenueCategoryID]),
  CONSTRAINT [FK_Revenue.EventID]
    FOREIGN KEY ([EventID])
      REFERENCES [Events]([EventID])
);

CREATE TABLE [Roles] (
  [RoleID] INT IDENTITY (1,1) NOT NULL,
  [Position] VARCHAR(50),
  [Description] VARCHAR(200),
  PRIMARY KEY ([RoleID])
);

CREATE TABLE [AcademicYear] (
  [TermID] INT IDENTITY (1,1) NOT NULL,
  [Description] VARCHAR(200),
  [StartDate] DATE,
  [EndDate] DATE,
  PRIMARY KEY ([TermID])
);

CREATE TABLE [Members] (
  [MemberID] INT IDENTITY (1,1) NOT NULL,
  [FName] VARCHAR(50),
  [LName] VARCHAR(50),
  [PhoneNumber] BIGINT,   
  [Email] VARCHAR(100),
  [JoinDate] DATE,
  [MembershipNumber] INT,
  PRIMARY KEY ([MemberID])
);

CREATE TABLE [MembershipStatus] (
  [StatusID] INT IDENTITY (1,1) NOT NULL,
  [MemberID] INT,
  [RoleID] INT,
  [TermID] INT,
  [IsActive] BIT,
  [Description] VARCHAR(200),
  PRIMARY KEY ([StatusID]),
  CONSTRAINT [FK_MembershipStatus.RoleID]
    FOREIGN KEY ([RoleID])
      REFERENCES [Roles]([RoleID]),
  CONSTRAINT [FK_MembershipStatus.TermID]
    FOREIGN KEY ([TermID])
      REFERENCES [AcademicYear]([TermID]),
  CONSTRAINT [FK_MembershipStatus.MemberID]
    FOREIGN KEY ([MemberID])
      REFERENCES [Members]([MemberID])
);

CREATE TABLE [Alumni] (
  [AlumniID] INT IDENTITY (1,1) NOT NULL,
  [MemberID] INT,
  [GraduationYear] INT,
  [Degree] VARCHAR(50),
  [MajorCompleted] VARCHAR(200),
  PRIMARY KEY ([AlumniID]),
  CONSTRAINT [FK_Alumni.MemberID]
    FOREIGN KEY ([MemberID])
      REFERENCES [Members]([MemberID])
);

CREATE TABLE [ExpenseCategories] (
  [ExpenseCategoryID] INT IDENTITY (1,1) NOT NULL,
  [Category] VARCHAR(200),
  [Description] VARCHAR(200),
  PRIMARY KEY ([ExpenseCategoryID])
);

CREATE TABLE [Expenses] (
  [ExpenseID] INT IDENTITY (1,1) NOT NULL,
  [ExpenseCategoryID] INT,
  [EventID] INT,
  [Expense] MONEY,
  [Description] VARCHAR(MAX),
  PRIMARY KEY ([ExpenseID]),
  CONSTRAINT [FK_Expenses.ExpenseCategoryID]
    FOREIGN KEY ([ExpenseCategoryID])
      REFERENCES [ExpenseCategories]([ExpenseCategoryID]),
  CONSTRAINT [FK_Expenses.EventID]
    FOREIGN KEY ([EventID])
      REFERENCES [Events]([EventID])
);

CREATE TABLE [EventRegistrations] (
  [RegistrationID] INT IDENTITY (1,1) NOT NULL,
  [MemberID] INT,
  [EventID] INT,
  PRIMARY KEY ([RegistrationID]),
  CONSTRAINT [FK_EventRegistrations.MemberID]
    FOREIGN KEY ([MemberID])
      REFERENCES [Members]([MemberID]),
  CONSTRAINT [FK_EventRegistrations.EventID]
    FOREIGN KEY ([EventID])
      REFERENCES [Events]([EventID])
);

